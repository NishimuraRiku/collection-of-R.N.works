//============================================================
//--------------------| *** 設定 *** |------------------------
//============================================================
//========================================
// SetMode関数 - モードの設定処理 -
//========================================
void SetMode(MODE mode)
{
	// 現在のモードの終了処理
	ModeUninit(g_RNmain.mode);

	// 現在のモードを切り替える
	g_RNmain.mode = mode;

	// 新しいモードの初期化処理
	ModeInit(mode);
}
