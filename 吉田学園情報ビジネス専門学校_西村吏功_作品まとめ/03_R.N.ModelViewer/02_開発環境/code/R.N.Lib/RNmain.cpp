//============================================================
//========================================
// RNInit関数 - R.N.Libの初期化処理 -
// Author:RIKU NISHIMURA
//========================================
void RNInit(HINSTANCE hInstance)
{
	// パラメーター初期化
	g_RNmain = InitParameterRNmain();

	InitSetting();	// 設定
	InitCamera2D();	// カメラ(2D)
	InitCamera3D();	// カメラ(3D)
	InitInput(		// 入力関連の初期化処理
		hInstance,
		*GetWindowHandle());
	InitLight();	// 光
	InitObject3D();	// オブジェクト3D
	InitPhysics();	// 物理関連
	InitSound(		// サウンド
		*GetWindowHandle());
	InitText();		// テキスト
	InitFade(		// フェード
		INIT_MODE);
}

//========================================
// RNUninit関数 - R.N.Libの終了処理 -
// Author:RIKU NISHIMURA
//========================================
void RNUninit(void)
{
	UninitCamera2D();	// カメラ(2D)
	UninitCamera3D();	// カメラ(3D)
	UninitFade();		// フェード
	UninitInput();		// 入力関連
	UninitObject3D();	// オブジェクト3D
	UninitLight();		// 光
	UninitSetting();	// 設定
	UninitSound();		// サウンド
	UninitText();		// テキスト
}

//========================================
// RNUpdate関数 - R.N.Libの更新処理 -
// Author:RIKU NISHIMURA
//========================================
void RNUpdate(void)
{
	// 当たり判定の線の数を初期化
	InitHitTestLineNum();

	// モードに応じた更新処理
	ModeUpdate(g_RNmain.mode);

	UpdateSetting();	// 設定
	UpdateInput();		// 入力関連
	UpdateObject3D();	// オブジェクト3D
	UpdateCamera2D();	// カメラ(2D)
	UpdateCamera3D();	// カメラ(3D)
	UpdateLight();		// 光
	UpdateFade();		// フェード
	DrawHitTestSetUp();	// 当たり判定の描画準備処理
	HitTestSignOut();	// 当たり判定の登録解除処理
}

//========================================
// RNDraw関数 - R.N.Libの描画処理 -
// Author:RIKU NISHIMURA
//========================================
void RNDraw(void)
{
	// モードに応じた描画処理
	ModeDraw(g_RNmain.mode);

	DrawObject3D();	// オブジェクト3D
	DrawHitTest();	// 当たり判定
	DrawText_();	// テキスト
	DrawFade();		// フェード
}

